# 1. OPERADORES ARITMÉTICOS
a = 10
b = 3

print("Soma (a + b):", a + b)       # 10 + 3 = 13
print("Subtração (a - b):", a - b)  # 10 - 3 = 7
print("Multiplicação (a * b):", a * b)  # 10 * 3 = 30
print("Divisão (a / b):", a / b)    # 10 / 3 = 3.333...
print("Divisão inteira (a // b):", a // b)  # 10 // 3 = 3
print("Módulo (a % b):", a % b)     # 10 % 3 = 1 (resto)
print("Exponenciação (a ** b):", a ** b)  # 10 ** 3 = 1000

print("\n---\n")